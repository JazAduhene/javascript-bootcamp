// Program that prints one of 3 situtations, using if statement, with else if and else clause.
// Else if statement utilised to create the possible outcomes of program. For example, if its not x, and its not y, then it must be z.

let temp = 40;

if (temp <= 15) {
  console.log('It is freezing outside!');
} else if (temp >= 30) {
  console.log('It is hot outside!');
} else {
  console.log('Go for it. It is pretty nice');
}
