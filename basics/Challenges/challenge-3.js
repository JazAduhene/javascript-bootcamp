// Program that converts temperature
// Order of operation PEMDAS - Parentheses (), Exponents, Multiply *, Divide /, Add +, Subtract -

let fahrenheit = 50;
let celsius = (fahrenheit - 32) * 5 / 9; // Calculates celsius
let kelvin = (fahrenheit - 32) * 5 / 9 + 273.15; // Calculates Kelvin

console.log(celsius);
console.log(kelvin);